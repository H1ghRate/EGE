a = int(input('First number: '))

b = int(input('Second number: '))

s = a + b

print('Summa')
print(a, '+', b, '=', s)

s = a - b

print('Raznost')
print(a, '-', b, '=', s)

s = b - a

print(b, '-', a, '=', s)

s = a * b

print('Product')
print(a, '*', b, '=', s)

s = a / b

print('Delim')
print(a, '/', b, '=', s)

s = b / a

